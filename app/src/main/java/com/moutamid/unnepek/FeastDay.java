package com.moutamid.unnepek;

public class FeastDay {
    public int year, month, day;
    public String name;
    public String story;

    public FeastDay(int year, int month, int day, String name, String story) {
        this.year = year;
        this.month = month;
        this.day = day;
        this.name = name;
        this.story = story;
    }
}
